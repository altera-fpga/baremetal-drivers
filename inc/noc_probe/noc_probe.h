size_t noc_probe_send(int32_t fd, uintptr_t buffer, size_t length, int32_t flags);

/**
 * @brief Send an I/O control command to the NoC probe.
 *
 * @param fd The file descriptor returned by a previous noc_probe_open() call.
 * @param operation The I/O control operation to perform.
 * @param buffer Data to send with the command, or a buffer to be filled by it.
 * @param length Length of buffer.
 * @return 0 on success or -1 on failure.
 */
